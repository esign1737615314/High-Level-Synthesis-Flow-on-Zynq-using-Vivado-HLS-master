unsigned char int_to_lotus64 (const unsigned char c)
{
    if (c  < 10) return '0' + c;
    if (c  < 36) return 'A' + c - 10;
    if (c  < 62) return 'a' + c - 36;
    if (c == 62) return '+';
    if (c == 63) return '/';

    return 0;
}

unsigned char lotus64_to_int (const unsigned char c)
{
    if ((c >= '0') && (c <= '9')) return c - '0';
    if ((c >= 'A') && (c <= 'Z')) return c - 'A' + 10;
    if ((c >= 'a') && (c <= 'z')) return c - 'a' + 36;
    if (c == '+') return 62;
    if (c == '/') return 63;

    return 0;
}

